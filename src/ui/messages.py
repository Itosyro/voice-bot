# ── Style picker headers ──

def style_header(mode: str) -> str:
    headers = {
        "polish": (
            f"{MODE_ICON['polish']} <b>Полировка</b>\n\n"
            "Как обработать текст?"
        ),
        "prompt": (
            f"{MODE_ICON['prompt']} <b>Промпт</b>\n\n"
            "Под какую задачу промпт?"
        ),
        "humanizer": (
            f"{MODE_ICON['humanizer']} <b>Очеловечить</b>\n\n"
            "Насколько сильно переписать?"
        ),
        "translator": (
            f"{MODE_ICON['translator']} <b>Перевод</b>\n\n"
            "На какой язык перевести?"
        ),
    }
    mode_name = MODE_NAME.get(mode, mode.upper())
    return headers.get(mode, f"<b>{mode_name}</b>\n\nВыбери стиль:")
